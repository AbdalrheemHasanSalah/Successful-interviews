// Navigation functionality
function initializeNavigation() {
    // Mobile menu toggle
    if (navToggle && navMenu) {
        navToggle.addEventListener('click', function() {
            navMenu.classList.toggle('active');
            navToggle.classList.toggle('active');
        });
    }

    // Smooth scrolling for navigation links
    const navLinks = document.querySelectorAll('.nav-link');
    navLinks.forEach(link => {
        link.addEventListener('click', function(e) {
            e.preventDefault();
            const targetId = this.getAttribute('href').substring(1);
            scrollToSection(targetId);
            
            // Close mobile menu if open
            if (navMenu && navMenu.classList.contains('active')) {
                navMenu.classList.remove('active');
                navToggle.classList.remove('active');
            }
        });
    });
}

// Scroll to section function
function scrollToSection(sectionId) {
    const element = document.getElementById(sectionId);
    if (element) {
        const offsetTop = element.offsetTop - 80; // Account for fixed navbar
        window.scrollTo({
            top: offsetTop,
            behavior: 'smooth'
        });
    }
}

// Search functionality
function initializeSearch() {
    let searchTimeout;
    
    if (searchInput) {
        searchInput.addEventListener('input', function() {
            clearTimeout(searchTimeout);
            const query = this.value.trim();
            
            if (query.length === 0) {
                searchResults.innerHTML = '';
                return;
            }
            
            // Debounce search
            searchTimeout = setTimeout(() => {
                if (query.length >= 2) {
                    performSearch(query);
                }
            }, 300);
        });
    }
}

// Perform search
function performSearch(query) {
    const lowerQuery = query.toLowerCase();
    const results = questions.filter(q => 
        q.question.toLowerCase().includes(lowerQuery) || 
        q.answer.toLowerCase().includes(lowerQuery)
    );
    
    displaySearchResults(results, query);
}

// Display search results
function displaySearchResults(results, query) {
    if (!searchResults) return;
    
    if (results.length === 0) {
        searchResults.innerHTML = `
            <div class="search-result">
                <p>لم يتم العثور على نتائج للبحث: "${query}"</p>
            </div>
        `;
        return;
    }
    
    const resultsHTML = results.map(result => `
        <div class="search-result">
            <h4>${highlightText(result.question, query)}</h4>
            <p>${highlightText(result.answer, query)}</p>
        </div>
    `).join('');
    
    searchResults.innerHTML = `
        <h3>نتائج البحث (${results.length}):</h3>
        ${resultsHTML}
    `;
}

// Highlight search terms
function highlightText(text, query) {
    const regex = new RegExp(`(${query})`, 'gi');
    return text.replace(regex, '<mark>$1</mark>');
}

// Load questions into the questions section
function loadQuestions() {
    if (!questionsContainer) return;
    
    const questionsHTML = commonquestions.map(commonquestions => `
        <div class="question-item">
            <div class="question-header" onclick="toggleQuestion(${commonquestions.id})">
                <h3>${commonquestions.question}</h3>
                <i class="fas fa-chevron-down question-icon" id="icon-${commonquestions.id}"></i>
            </div>
            <div class="question-answer" id="answer-${commonquestions.id}">
                <p>${commonquestions.answer}</p>
            </div>
        </div>
    `).join('');
    
    questionsContainer.innerHTML = questionsHTML;
}

// Toggle question visibility
function toggleQuestion(questionId) {
    const answerElement = document.getElementById(`answer-${questionId}`);
    const iconElement = document.getElementById(`icon-${questionId}`);
    
    if (answerElement && iconElement) {
        answerElement.classList.toggle('active');
        iconElement.classList.toggle('active');
    }
}

// Random question functionality
function showRandomQuestion() {
    if (questions.length === 0) return;
    
    const randomIndex = Math.floor(Math.random() * questions.length);
    const randomQuestion = questions[randomIndex];
    
    if (randomQuestionContent && randomQuestionModal) {
        randomQuestionContent.innerHTML = `
            <div class="random-question">
                <h4>السؤال:</h4>
                <p><strong>${randomQuestion.question}</strong></p>
                <h4>الإجابة المقترحة:</h4>
                <p>${randomQuestion.answer}</p>
            </div>
        `;
        
        randomQuestionModal.style.display = 'block';
    }
}

// Close modal
function closeModal() {
    if (randomQuestionModal) {
        randomQuestionModal.style.display = 'none';
    }
}

